package basics1;

public class EvenOdd {
// write a program to check if a number is even or odd
	
	public static void main(String[] args) {
		
		int evenOdd;
		evenOdd = 9;
		if (evenOdd % 2 == 0) 
			System.out.println(evenOdd + " is even");
		else
			System.out.println(evenOdd + " is odd");
		
	}
}
